class P3 
{
	public static void main(String[] args) {
		// if else if ladder statement
		int test=5;
		if (test==1)
		{ System.out.println("Value of test is 1");
		}
		else if (test==2)
		{ System.out.println("Value of test is 2");
		}
		else if (test==3)
		{ System.out.println("Value of test is 3");
		}
		else if (test==4)
		{ System.out.println("Value of test is 4");
		}
		else if (test==5)
		{ System.out.println("Value of test is 5");
		}
		else
		System.out.println("Value not Matched");
		}
}

